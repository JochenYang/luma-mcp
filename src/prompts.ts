/**
 * 构建图片分析提示词
 */
export function buildAnalysisPrompt(question?: string): string {
  if (question) {
    // 有用户问题时，直接回答问题
    return `
<image_analysis>
请仔细观察这张图片，并回答以下问题：

${question}

分析要求：
1. 系统性观察：从整体到细节，全面观察图片内容
2. 准确识别：精确识别所有文字、符号、代码、数字等
3. 结构化输出：使用清晰的标题和列表组织信息
4. 中文表达：使用清晰准确的中文描述
5. 具体可执行：如果涉及代码或问题，提供具体的解决方案

请开始分析：
</image_analysis>
`.trim();
  } else {
    // 没有问题时，通用描述
    return `
<image_analysis>
请详细描述这张图片的内容，包括：

1. **主要内容**：图片展示了什么
2. **关键元素**：重要的视觉元素、文字、符号
3. **布局结构**：元素的组织和排列
4. **文字内容**：提取所有可见文字

请用中文清晰描述。
</image_analysis>
`.trim();
  }
}
